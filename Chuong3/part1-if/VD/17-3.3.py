a=int(input('Tieu thu='))
if 1<=a<=100:
    print('Phai tra=', (a*550)+0.1*550)
elif 101<=a<=150:
    print('Phai tra=',((550*100)+(750*(a-100)))+0.1*((550*100)+(750*(a-100))),sep=(''))
elif 151<=a<=200:
    print('Phai tra=',((550*100)+(750*50)+(950*(a-150)))+(0.1*((550*100)+(750*50)+(950*(a-150)))),sep=(''))
else:
    print('Phai tra=',((550*100)+(750*50)+(1350*(a-200))+0.1*((550*100)+(750*50)+(1350*(a-200)))),sep=(''))
 
 
